if not C_Item then C_Item = {} end

function C_Item.IsBound(item)
    if not item then return false end

    local tooltip = C_Item:GetCacheTooltip() or CreateFrame("GameTooltip", "C_ItemShimTooltip", UIParent, "GameTooltipTemplate")
    tooltip:SetOwner(UIParent,"ANCHOR_NONE")
    tooltip:ClearLines()

    if item:IsBagAndSlot() then
        tooltip:SetBagItem(item:GetBagAndSlot())

    elseif item:IsEquipmentSlot() then
        tooltip:SetInventoryItem("player", item:GetEquipmentSlot())

    else
        local itemLink = type(item) == "string" and item or (item.GetItemLink and item:GetItemLink())
        if not itemLink then return false end
        tooltip:SetHyperlink(itemLink)
    end
    tooltip:Show()

    for i = 1, tooltip:NumLines() do
        local line = _G[tooltip:GetName().."TextLeft"..i]
        if line then
            local text = line:GetText()
            if text and (text:find(ITEM_SOULBOUND) or text:find("Realm Bound")) then
                tooltip:Hide()
                return true
            end
        end
    end

    tooltip:Hide()
    return false
end

--[[ ContinuableContainer ]]

ContinuableContainer = {}

function ContinuableContainer:Create()
    local container = {}
    container.items = {}
    setmetatable(container, {__index = self})
    return container
end

function ContinuableContainer:AddContinuable(item)
    if item then
        table.insert(self.items, item)
    end
end

function ContinuableContainer:ContinueOnLoad(func)
    local items = self.items
    local count = #items
    if count == 0 then
        func()
        return
    end

    local loaded = 0
    for _, item in ipairs(items) do
        item:ContinueOnLoad(function()
            loaded = loaded + 1
            if loaded >= count then
                func()
            end
        end)
    end
end

