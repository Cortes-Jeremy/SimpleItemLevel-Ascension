--[[ Heirlooms ilvl ]]

 function GetHeirloomEffectiveIlvl(unit)
    local level = UnitLevel(unit)
    local budget

    if level <= 60 then
        budget = 2 + (level - 1) * 0.61
        return math.floor(budget * 1.55)

    elseif level <= 70 then
        budget = 38 + (level - 60) * 2.4
        return math.floor(budget * 1.85)

    else
        budget = 62 + (level - 70) * 6.9
        return math.floor(budget * 1.52)
    end
end

