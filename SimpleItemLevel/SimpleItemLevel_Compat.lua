--[[ ItemMixin <- ItemLocation ]]

function ItemMixin:ClearLoc()
	self.bag = nil
	self.slot = nil
	self.equipmentSlot = nil
end

function ItemMixin:SetBagAndSlot(bag, slot)
	self:ClearLoc()

	self.bag = bag
	self.slot = slot
end
function ItemMixin:GetBagAndSlot()
	return self.bag, self.slot
end
function ItemMixin:IsBagAndSlot()
	return self.bag ~= nil and self.slot ~= nil
end

function ItemMixin:SetEquipmentSlot(unit, slot)
	self:ClearLoc()
    self.equipmentUnit = unit or "player"
	self.equipmentSlot = slot
end
function ItemMixin:GetEquipmentSlot()
	return self.equipmentSlot
end
function ItemMixin:IsEquipmentSlot()
	return self.equipmentSlot ~= nil
end

function ItemMixin:IsValid()
	if self:IsEquipmentSlot() then
		return GetInventoryItemLink("player", self:GetEquipmentSlot()) ~= nil
	elseif self:IsBagAndSlot() then
		return GetContainerItemID(self:GetBagAndSlot())
	end
end

--[[ ItemMixin ]]

function ItemMixin:GetCurrentItemLevel()
    local itemName, itemLink, itemRarity, itemLevel, itemMinLevel, itemType, itemSubType, itemCount, itemEquipLoc, itemTexture, itemSellValue = GetItemInfo(self.itemID)
    return itemLevel or 0
end

function ItemMixin:IsItemEmpty()
    return self:IsEmpty()
end

function ItemMixin:ContinueOnItemLoad(func)
    if type(func) ~= "function" then
        error("ContinueOnItemLoad expects a function", 2)
    end
    return self:ContinueOnLoad(func)
end

function ItemMixin:GetItemQuality()
    if self:IsEmpty() then return 1 end
    local quality = select(3, GetItemInfo(self.itemID)) or GetItemQuality(self.itemID)
    return quality or 1
end

function ItemMixin:GetItemLink()
    local itemID = self.itemID or (self.GetItemID and self:GetItemID())
    if not itemID then return nil end

    local _, link = GetItemInfo(itemID)
    return link
end

function ItemMixin:GetInstanceItemLink(unit)
    unit = unit or "player"

    -- Item équipé
    if self.equipmentSlot then
        return GetInventoryItemLink(unit, self.equipmentSlot)
    end

    -- Item dans les sacs
    if self.bag and self.slot then
        return GetContainerItemLink(self.bag, self.slot)
    end

    -- Fallback (template)
    return self:GetItemLink()
end

function ItemMixin:GetItemLocation()
    return self
end

function ItemMixin:IsItemInPlayersControl()
    if self:IsBagAndSlot() then
        return GetContainerItemID(self:GetBagAndSlot()) ~= nil
    elseif self:IsEquipmentSlot() then
        return GetInventoryItemID("player", self:GetEquipmentSlot()) ~= nil
    end
    return false
end

function ExtendItem(item)
    if not item or item.__simpleilvl_extended then return end

    item.__simpleilvl_extended = true

    item.GetCurrentItemLevel    = ItemMixin.GetCurrentItemLevel
    item.IsItemEmpty            = ItemMixin.IsItemEmpty
    item.ContinueOnItemLoad     = ItemMixin.ContinueOnItemLoad
    item.GetItemQuality         = ItemMixin.GetItemQuality
    item.GetItemLink            = ItemMixin.GetItemLink
    item.GetInstanceItemLink    = ItemMixin.GetInstanceItemLink
    item.GetItemLocation        = ItemMixin.GetItemLocation
    item.IsItemInPlayersControl = ItemMixin.IsItemInPlayersControl

    item.ClearLoc               = ItemMixin.ClearLoc
    item.SetBagAndSlot          = ItemMixin.SetBagAndSlot
    item.GetBagAndSlot          = ItemMixin.GetBagAndSlot
    item.IsBagAndSlot           = ItemMixin.IsBagAndSlot
    item.SetEquipmentSlot       = ItemMixin.SetEquipmentSlot
    item.GetEquipmentSlot       = ItemMixin.GetEquipmentSlot
    item.IsEquipmentSlot        = ItemMixin.IsEquipmentSlot
    item.IsValid                = ItemMixin.IsValid
end

--[[ Item ]]

function Item:CreateFromEquipmentSlot(unit, slotID)
    if not slotID then return end

    local itemLink = GetInventoryItemLink(unit, slotID)
    if not itemLink then return end

    local item = self:CreateFromLink(itemLink)
    if not item then return end

    ExtendItem(item)

    item:SetEquipmentSlot(unit, slotID)

    return item
end

function Item:CreateFromItemLink(itemLink)
    if not itemLink then return end
    return self:CreateFromLink(itemLink)
end

function Item:CreateFromItemID(itemID)
    if not itemID then return end
    return self:CreateFromID(itemID)
end

function Item:GetItemQuality()
    local itemID = self:GetItemID()
    if not itemID then return 1 end

    local quality = select(3, GetItemInfo(itemID)) or GetItemQuality(itemID)
    return quality or 1
end

function Item:CreateFromBagAndSlot(bag, slot)
    if bag == nil or slot == nil then return end

    local itemLink = GetContainerItemLink(bag, slot)
    if not itemLink then return end

    local item = self:CreateFromLink(itemLink)
    if not item then return end

    ExtendItem(item)

    item:SetBagAndSlot(bag, slot)

    return item
end

